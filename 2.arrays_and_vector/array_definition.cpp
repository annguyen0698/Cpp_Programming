/*
DEFINITION
-- compound data type or structured data type. This means that it's a data type that's made up of other data types/
for example: a collection of test scores where each test score is an integer is considered a compound data type made up of integers

-- all elements are of the same type 

-- each element can be accessed directly
=====================================
CHARACTERISTICS: 
--fixed size
-- elements are all the same size
-- stored contiguosly in memory 
-- individual elements can be accessed by their position or index

-- first element is at index 0 
-- last element is at index size -1
-- no checking to see if you are out of bounds

-- always initialize arrays 
-- very efficient
-- iteration (looping) is often used to process 
*/